"""–µ–æ–±—ã—á–Ω–æ–µ —Å—Ä–∞–≤–Ω–µ–Ω–∏–µ üìä
–ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞—é—Ç—Å—è 2 —Å—Ç—Ä–æ–∫–∏. –í–∞–º –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ —Å—Ä–∞–≤–Ω–∏—Ç—å —ç—Ç–∏ —Å—Ç—Ä–æ–∫–∏ –ø–æ—Å–∏–º–≤–æ–ª—å–Ω–æ,
–Ω–µ —É—á–∏—Ç—ã–≤–∞—è —Ä–µ–≥–∏—Å—Ç—Ä –∏ –∏–≥–Ω–æ—Ä–∏—Ä—É—è –≤—Å–µ –Ω–µ–±—É–∫–≤–µ–Ω–Ω—ã–µ —Å–∏–º–≤–æ–ª—ã. –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ ¬´YES¬ª,
–µ—Å–ª–∏ —Å—Ç—Ä–æ–∫–∏ –æ–∫–∞–∂—É—Ç—Å—è —Ä–∞–≤–Ω—ã –≤ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–µ —Ç–∞–∫–æ–π –ø—Ä–æ–≤–µ—Ä–∫–∏, –∏–ª–∏ ¬´NO¬ª –≤ –ø—Ä–æ—Ç–∏–≤–Ω–æ–º —Å–ª—É—á–∞–µ.

–§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
–ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞—é—Ç—Å—è 2 —Å—Ç—Ä–æ–∫–∏, –∫–∞–∂–¥–∞—è –Ω–∞ –æ—Ç–¥–µ–ª—å–Ω–æ–π —Å—Ç—Ä–æ–∫–µ.

–§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
–ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ ¬´YES¬ª –∏–ª–∏ ¬´NO¬ª –≤ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–∏ —Å —É—Å–ª–æ–≤–∏–µ–º –∑–∞–¥–∞—á–∏."""
s1, s2 = input(), input()

str_s1 = [ord(i) for i in s1.lower() if i.isalpha()]
str_s2 = [ord(i) for i in s2.lower() if i.isalpha()]

cnt1 = 0
cnt2 = 0

for i in range(len(str_s1)):
    cnt1 += str_s1[i]

for i in range(len(str_s2)):
    cnt2 += str_s2[i]

if cnt1 == cnt2:
    print("YES")
else:
    print("NO")

# –í—Ç–æ—Ä–æ–π —Å–ø–æ—Å–æ–± —Ä–µ—à–µ–Ω–∏—è - –≤–∑—è—Ç–æ –∏–∑ —Ñ–æ—Ä—É–º–∞ —Ä–µ—à–µ–Ω–∏—è
st_1 = [i.lower() for i in input() if i.isalpha()]
st_2 = [j.lower() for j in input() if j.isalpha()]

print("YES" if st_1 == st_2 else "NO")
