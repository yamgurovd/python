"""–°–±–æ–π –≤ —Å–∏—Å—Ç–µ–º–µ ‚ö†Ô∏èüå∂Ô∏èüå∂Ô∏è
–ü–æ—Å–ª–µ –Ω–µ–¥–∞–≤–Ω–µ–≥–æ —Å–±–æ—è –≤ –æ–ø–µ—Ä–∞—Ü–∏–æ–Ω–Ω–æ–π —Å–∏—Å—Ç–µ–º–µ –æ—Ç –∫–æ–º–ø–∞–Ω–∏–∏ ¬´Oursoft¬ª —É –ì–≤–∏–¥–æ —Å–±–∏–ª–∞—Å—å –∫–æ–¥–∏—Ä–æ–≤–∫–∞ –Ω–∞ –∫–æ–º–ø—å—é—Ç–µ—Ä–µ.
–¢–µ–ø–µ—Ä—å –≤—Å–µ –±—É–∫–≤—ã —Ä—É—Å—Å–∫–æ–≥–æ –∞–ª—Ñ–∞–≤–∏—Ç–∞ –æ—Ç–æ–±—Ä–∞–∂–∞—é—Ç—Å—è –≤ –Ω–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ–º –≤–∏–¥–µ:

[u-<–Ω–æ–º–µ—Ä —Å–∏–º–≤–æ–ª–∞ –≤ —Ç–∞–±–ª–∏—Ü–µ Unicode>]
–ì–≤–∏–¥–æ –µ—â–µ –Ω–µ –Ω–∞—É—á–∏–ª—Å—è —á–∏—Ç–∞—Ç—å —Å–∏–º–≤–æ–ª—ã –≤ —Ç–∞–∫–æ–º —Ñ–æ—Ä–º–∞—Ç–µ, –ø–æ—ç—Ç–æ–º—É –ø—Ä–æ—Å–∏—Ç –≤–∞—Å –Ω–∞–ø–∏—Å–∞—Ç—å –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –±—É–¥–µ—Ç
"—Ä–∞—Å—à–∏—Ñ—Ä–æ–≤—ã–≤–∞—Ç—å" –¥–ª—è –Ω–µ–≥–æ –≤—Å–µ —Ç–µ–∫—Å—Ç—ã –Ω–∞ –∫–æ–º–ø—å—é—Ç–µ—Ä–µ.

–ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞–µ—Ç—Å—è —Å—Ç—Ä–æ–∫–∞ —Ç–µ–∫—Å—Ç–∞. –†–∞—Å—à–∏—Ñ—Ä—É–π—Ç–µ —Ç–µ–∫—Å—Ç, –∑–∞–º–µ–Ω–∏–≤ –≤—Å–µ –∫–æ–Ω—Å—Ç—Ä—É–∫—Ü–∏–∏ [u-<–Ω–æ–º–µ—Ä —Å–∏–º–≤–æ–ª–∞ –≤ —Ç–∞–±–ª–∏—Ü–µ Unicode>]
–Ω–∞ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â–∏–µ –±—É–∫–≤—ã —Ä—É—Å—Å–∫–æ–≥–æ –∞–ª—Ñ–∞–≤–∏—Ç–∞, –∏ –≤—ã–≤–µ–¥–∏—Ç–µ –µ–≥–æ.

–§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
–ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞–µ—Ç—Å—è —Å—Ç—Ä–æ–∫–∞ —Ç–µ–∫—Å—Ç–∞, –≤ –∫–æ—Ç–æ—Ä–æ–π –º–æ–≥—É—Ç –±—ã—Ç—å –∑–∞—à–∏—Ñ—Ä–æ–≤–∞–Ω—ã —Å–∏–º–≤–æ–ª—ã —Ä—É—Å—Å–∫–æ–≥–æ –∞–ª—Ñ–∞–≤–∏—Ç–∞.

–§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
–ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ —Å—Ç—Ä–æ–∫—É —Ç–µ–∫—Å—Ç–∞, —Ä–∞—Å—à–∏—Ñ—Ä–æ–≤–∞–≤ —Å–∏–º–≤–æ–ª—ã —Ä—É—Å—Å–∫–æ–≥–æ –∞–ª—Ñ–∞–≤–∏—Ç–∞.

–ü—Ä–∏–º–µ—á–∞–Ω–∏–µ. –ë—É–¥–µ–º —Å—á–∏—Ç–∞—Ç—å, —á—Ç–æ –±—É–∫–≤—ã –Å –Ω–µ—Ç –≤ —Ä—É—Å—Å–∫–æ–º –∞–ª—Ñ–∞–≤–∏—Ç–µ. ü§´"""

text = input()
result = ""
i = 0
while i < len(text):
    if text.startswith('[u-', i):
        start = i + 3
        end = text.find(']', start)
        if end != -1:
            code = int(text[start:end])
            result += chr(code)
            i = end + 1
        else:
            break
    else:
        result += text[i]
        i += 1

print(result)

# –í—Ç–æ—Ä–æ–π –≤–∞—Ä–∏–∞–Ω—Ç —Ä–µ—à–µ–Ω–∏—è - –≤–∑—è—Ç –∏–∑ —Ñ–æ—Ä—É–º–∞ —Ä–µ—à–µ–Ω–∏–π
# —Å—á–∏—Ç—ã–≤–∞–µ–º —Å—Ç—Ä–æ–∫—É
s = input()

# –¥–æ–±–∞–≤–ª—è–µ–º –¥–ª—è —É–¥–æ–±—Å—Ç–≤–∞ –∫–∏—Ä–∏–ª. –∞–ª—Ñ–∞–≤–∏—Ç –æ–±–∞ —Ä–µ–≥–∏—Å—Ç—Ä–∞ –≤ —Å—Ç—Ä–æ–∫—É –∞–ª—Ñ
alph = '–∞–±–≤–≥–¥–µ–∂–∑–∏–π–∫–ª–º–Ω–æ–ø—Ä—Å—Ç—É—Ñ—Ö—Ü—á—à—â—ä—ã—å—ç—é—è–ê–ë–í–ì–î–ï–ñ–ó–ò–ô–ö–õ–ú–ù–û–ü–†–°–¢–£–§–•–¶–ß–®–©–™–´–¨–≠–Æ–Ø'

# –∑–∞–ø—É—Å–∫–∞–µ–º —Ü–∏–∫–ª –Ω–∞ 64 –∏—Ç–µ—Ä–∞—Ü–∏–∏, –¥–ª—è –∫–∞–∂–¥–æ–≥–æ —Å–∏–º–≤–æ–ª–∞ –∞–ª—Ñ–∞–≤–∏—Ç–∞
for i in range(64):

    # –±–µ—Ä–µ–º –∫–æ–¥ –∫–∞–∂–¥–æ–≥–æ —Å–∏–º–≤–æ–ª–∞ –∫–∏—Ä–∏–ª. –∞–ª—Ñ–∞–≤–∏—Ç–∞ –≤ —é–Ω–∏–∫–æ–¥–µ
    unicode = ord(alph[i])

    # –ø–µ—Ä–µ–≤–æ–¥–∏–º –µ–≥–æ —á–∏—Å–ª–µ–Ω–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –≤ —Å—Ç—Ä. —Ç–∏–ø –¥–∞–Ω–Ω—ã—Ö –∏ –ø—Ä–æ–≤–µ—Ä—è–µ–º
    # –µ—Å—Ç—å –ª–∏ —Ç–∞–∫–∞—è –ø–æ–¥—Å—Ç—Ä–æ–∫–∞ –≤ –∏—Å—Ö. —Å—Ç—Ä–æ–∫–µ
    if str(unicode) in s:
        # –µ—Å–ª–∏ –µ—Å—Ç—å, —Ç–æ –º–µ–Ω—è–µ–º –ø–æ–¥—Å—Ç—Ä–æ–∫—É "[u-<unicode>]" –Ω–∞ —Å–∏–º–≤–æ–ª —Å –æ–ø—Ä–µ–¥. –∑–Ω–∞—á–Ω–∏–µ–º –≤ –∫–∞–∂–¥–æ–π –∏—Ç–µ—Ä–∞—Ü–∏–∏ —Ü–∏–∫–ª–∞
        # –∏ –∑–∞–ø–∏—Å—ã–≤–∞–µ–º –∏–∑–º–µ–Ω–µ–Ω–∏—è –≤ –∏—Å—Ö. —Å—Ç—Ä–æ–∫—É
        s = s.replace(f'[u-{unicode}]', f'{chr(unicode)}')

# –∏ –≤—ã–≤–æ–¥–∏–º
print(s)
