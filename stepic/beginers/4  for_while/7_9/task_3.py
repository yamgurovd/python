# –°—á–∏—Ç—ã–≤–∞–µ–º –¥–≤–∞ —á–∏—Å–ª–∞ –∏ –∑–∞–ø–∏—Å—ã–≤–∞–µ–º –∏—Ö –≤ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ 'a' –∏ 'b'
# –°–æ–∑–¥–∞–µ–º —Å—á–µ—Ç—á–∏–∫ –¥–ª—è –∫–æ–Ω–µ—á–Ω–æ–≥–æ –ø—Ä–∏—Å–≤–∞–∏–≤–∞–Ω–∏—è —Å—É–º–º—ã counter —Ä–∞–≤–Ω—ã–π 0
# –°–æ–∑–¥–∞–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é –¥–ª—è –ø—Ä–∏–Ω—è—Ç–∏—è —Ç–æ–≥–æ —Å–∞–º–æ–≥–æ —á–∏—Å–ª–∞ —É –∫–æ—Ç–æ—Ä–æ–≥–æ —Å—É–º–º–∞ –¥–µ–ª–∏—Ç–µ–ª–µ–π –±–æ–ª—å—à–µ 'largest' = 0
# –°–æ–∑–¥–∞–¥–∏–º –≤–Ω–µ—à–Ω–∏–π —Ü–∏–∫–ª 'i' –æ—Ç 'a' –¥–æ 'b' –≤–∫–ª—é—á–∏—Ç–µ–ª—å–Ω–æ
# –°–æ–∑–¥–∞–¥–∏–º –≤–Ω—É—Ç—Ä–∏ —Ü–∏–∫–ª–∞ —Å—á–µ—Ç—á–∏–∫ (–æ–±–Ω—É–ª—è—Ç–æ—Ä) total = 0
# –°–æ–∑–¥–∞–¥–∏–º –≤–Ω—É—Ç—Ä–µ–Ω–Ω–∏–π —Ü–∏–∫–ª 'j' –æ—Ç 1 –¥–æ 'i' –≤–∫–ª—é—á–∏—Ç–µ–ª—å–Ω–æ
# –í–Ω—É—Ç—Ä–∏ –¥–µ–ª–∞–µ–º 1 —É—Å–ª–æ–≤–∏–µ, –µ—Å–ª–∏ i % j == 0
# total + j –ø–æ–¥—Å—á–∏—Ç—ã–≤–∞–µ–º —Å—É–º–º—É –¥–µ–ª–∏—Ç–µ–ª–µ–π –∏–º–µ–Ω–Ω–æ —ç—Ç–æ–≥–æ —á–∏—Å–ª–∞
# –°–¥–µ–ª–∞–µ–º 2-–µ —É—Å–ª–æ–≤–∏–µ –µ—Å–ª–∏ total >= counter, —Ç–æ–±–∏—à—å –µ—Å–ª–∏ —Å—É–º–º–∞ –±–æ–ª—å—à–µ –∫–æ–Ω–µ—á–Ω–æ–≥–æ —Å—á–µ—Ç—á–∏–∫–∞ and 'i' >= largest, –µ—Å–ª–∏ —á–∏—Å–ª–æ –Ω–∞ –∫–æ—Ç–æ—Ä–æ–µ –º—ã –¥–µ–ª–∏–º –±–æ–ª—å—à–µ –∫–æ–Ω–µ—á–Ω–æ–≥–æ —á–∏—Å–ª–∞
