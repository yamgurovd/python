a = int(input())
b = int(input())

sum = 0
maxi = 0
maxsum = 0
for i in range(b, a - 1, -1):
    for j in range(b, 0, -1):
        if i % j == 0:
            sum = sum + j
    if sum > maxsum:
        maxsum = sum
        maxi = i
    sum = 0

print(maxi, maxsum, end=" ")
