"""Ревью кода-4 🌶️🌶️
На обработку поступает натуральное число. Нужно написать программу, которая выводит на экран максимальную цифру числа, кратную 3.
Если в числе нет цифр, кратных 3, требуется на экран вывести «NO». Программист торопился и написал программу неправильно.

Найдите все ошибки в этой программе (их ровно 5). Известно, что каждая ошибка затрагивает только одну строку и
может быть исправлена без изменения других строк.

Примечание 1. Число 0 делится на любое натуральное число.

Примечание 2. При необходимости вы можете добавить нужные строки кода"""

num = int(input())

mx = 0

for i in range(1, num + 1):
    if i % 3 == 0:
        mx = i

if mx == 0:
    print("NO")
else:
    print(mx)

# Решение от Гигачата
number = int(input("Введите число: "))
max_digit = None

for digit in str(number):
    if int(digit) % 3 == 0:
        max_digit = int(digit)

if max_digit is not None:
    print(max_digit)
else:
    print("NO")

# Мое предыдущее решение
n = int(input())
max_digit = -1
while n > 0:
    digit = n % 10
    if digit % 3 == 0:
        if digit > max_digit:
            max_digit = digit
    n = n // 10
if max_digit == -1:
    print('NO')
else:
    print(max_digit)
