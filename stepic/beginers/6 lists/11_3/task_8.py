"""k-–∞—è –±—É–∫–≤–∞ —Å–ª–æ–≤–∞ üå∂Ô∏è
–ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞–µ—Ç—Å—è –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ n –∏ n —Å—Ç—Ä–æ–∫, –∞ –∑–∞—Ç–µ–º —á–∏—Å–ª–æ k.
–ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –≤—ã–≤–æ–¥–∏—Ç k-—É—é –±—É–∫–≤—É –∏–∑ –≤–≤–µ–¥–µ–Ω–Ω—ã—Ö —Å—Ç—Ä–æ–∫ –Ω–∞ –æ–¥–Ω–æ–π —Å—Ç—Ä–æ–∫–µ –±–µ–∑ –ø—Ä–æ–±–µ–ª–æ–≤.

–§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
–ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞–µ—Ç—Å—è –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ n,  –¥–∞–ª–µ–µ n —Å—Ç—Ä–æ–∫, –∫–∞–∂–¥–∞—è –Ω–∞ –æ—Ç–¥–µ–ª—å–Ω–æ–π —Å—Ç—Ä–æ–∫–µ.
–í –∫–æ–Ω—Ü–µ –≤–≤–æ–¥–∏—Ç—Å—è –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ k ‚Äì –Ω–æ–º–µ—Ä –±—É–∫–≤—ã (–Ω—É–º–µ—Ä–∞—Ü–∏—è –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è —Å –µ–¥–∏–Ω–∏—Ü—ã).

–§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
–ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ —Ç–µ–∫—Å—Ç –≤ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–∏ —Å —É—Å–ª–æ–≤–∏–µ–º –∑–∞–¥–∞—á–∏.

–ü—Ä–∏–º–µ—á–∞–Ω–∏–µ. –ï—Å–ª–∏ –Ω–µ–∫–æ—Ç–æ—Ä—ã–µ —Å—Ç—Ä–æ–∫–∏ —Å–ª–∏—à–∫–æ–º –∫–æ—Ä–æ—Ç–∫–∏–µ, –∏ –≤ –Ω–∏—Ö –Ω–µ—Ç —Å–∏–º–≤–æ–ª–∞ —Å –∑–∞–¥–∞–Ω–Ω—ã–º –Ω–æ–º–µ—Ä–æ–º,
—Ç–æ —Ç–∞–∫–∏–µ —Å—Ç—Ä–æ–∫–∏ –ø—Ä–∏ –≤—ã–≤–æ–¥–µ –Ω—É–∂–Ω–æ –∏–≥–Ω–æ—Ä–∏—Ä–æ–≤–∞—Ç—å.
"""

n = int(input())
li = []
for _ in range(n):
    li.append(input())
index = int(input())
res = ''
for s in li:
    if len(s) >= index:
        res += s[index - 1]

print(res)

# –í—Ç–æ—Ä–æ–π —Å–ø–æ—Å–æ–± —Ä–µ—à–µ–Ω–∏—è –∑–∞–¥–∞—á–∏ - –≤–∑—è—Ç–æ –∏–∑ —Ñ–æ—Ä—É–º–∞
data = [input() for i in range(int(input()))]
k = int(input())
for i in data:
    if len(i) >= k:
        print(i[k - 1], end='')

# –¢—Ä–µ—Ç–∏–π —Å–ø–æ—Å–æ–± —Ä–µ—à–µ–Ω–∏—è –∑–∞–¥–∞—á–∏ - –≤–∑—è—Ç–æ –∏–∑ –ì–∏–≥–∞—á–∞—Ç–∞
# –ß—Ç–µ–Ω–∏–µ –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
n = int(input())  # –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å—Ç—Ä–æ–∫
strings = [input().strip() for _ in range(n)]  # –°–ø–∏—Å–æ–∫ —Å—Ç—Ä–æ–∫
k = int(input())  # –ü–æ–∑–∏—Ü–∏—è –±—É–∫–≤—ã

# –°–±–æ—Ä k-–æ–π –±—É–∫–≤—ã –∏–∑ –∫–∞–∂–¥–æ–π —Å—Ç—Ä–æ–∫–∏
result = ''.join([s[k - 1] for s in strings if len(s) >= k])

# –í—ã–≤–æ–¥ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞
print(result)
