# –ö–æ–¥ –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–Ω—ã–π –≥–∏–≥–∞—á–∞—Ç–æ–º
def determine_color(number):
    if number == 0:
        return 'green'
    elif 1 <= number <= 10 and number % 2 != 0:
        return 'red'
    elif 11 <= number <= 18 and number % 2 == 0:
        return 'black'
    elif 19 <= number <= 28 and number % 2 != 0:
        return 'red'
    elif 29 <= number <= 36 and number % 2 == 0:
        return 'black'
    else:
        return 'error'

