"""Конкурсный отбор
Напишите программу, которая выводит список хорошистов и отличников в классе.

Формат входных данных
На вход программе подается натуральное число n, далее следует n строк с фамилией школьника и его оценкой на каждой из них.

Формат выходных данных
Программа должна вывести сначала все введённые строки с фамилиями и оценками учеников в том же порядке.
Затем следует пустая строка, а затем выводятся строки с фамилиями и оценками хорошистов и отличников (в том же порядке).

Примечание 1. Оценка ученика – это натуральное число от 1 до 5.

Примечание 2. Гарантируется, что в классе есть хотя бы один хорошист (обладатель оценки 4) или отличник (получивший 5)."""
students = [tuple(input().split()) for _ in range(int(input()))]

for student in students:
    print(*student)

print()

for name, grade in students:
    if int(grade) > 3:
        print(name, grade)


# Второй способ решения задачи - взято из форума
# Считываем количество учеников
n = int(input())

# Создаем список для хранения данных о школьниках в виде кортежей
students = []

# Считываем данные о школьниках
for _ in range(n):
    student_info = input().strip()  # Считываем строку с фамилией и оценкой
    name, grade = student_info.rsplit(maxsplit=1)  # Разделяем на фамилию и оценку
    grade = int(grade)  # Преобразуем оценку в целое число
    students.append((name, grade))  # Добавляем кортеж (фамилия, оценка) в список

# Выводим все введенные строки
for student in students:
    print(f"{student[0]} {student[1]}")  # Форматируем вывод

# Печатаем пустую строку
print()

# Выводим список хорошистов и отличников
for student in students:
    name, grade = student  # Распаковываем кортеж
    # Проверяем, является ли ученик хорошистом (4) или отличником (5)
    if grade == 4 or grade == 5:
        print(f"{name} {grade}")  # Форматируем вывод
