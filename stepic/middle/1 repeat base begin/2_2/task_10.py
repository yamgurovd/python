# –ü–µ—Ä–≤—ã–π —Å–ø–æ—Å–æ–± —Ä–µ—à–µ–Ω–∏—è –∑–∞–¥–∞—á–∏ - –≤–∑—è—Ç–æ –∏–∑ Chat GPT
def find_infected_refrigerators():
    n = int(input())  # –ß–∏—Ç–∞–µ–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Ö–æ–ª–æ–¥–∏–ª—å–Ω–∏–∫–æ–≤
    infected_indices = []  # –°–ø–∏—Å–æ–∫ –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è –Ω–æ–º–µ—Ä–æ–≤ –∑–∞—Ä–∞–∂–µ–Ω–Ω—ã—Ö —Ö–æ–ª–æ–¥–∏–ª—å–Ω–∏–∫–æ–≤

    for i in range(n):
        refrigerator_data = input()  # –ß–∏—Ç–∞–µ–º —Å—Ç—Ä–æ–∫—É —Å –¥–∞–Ω–Ω—ã–º–∏ —Ö–æ–ª–æ–¥–∏–ª—å–Ω–∏–∫–∞
        # –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç–∏ –±—É–∫–≤ "anton"
        if contains_anton(refrigerator_data):
            infected_indices.append(i + 1)  # –î–æ–±–∞–≤–ª—è–µ–º –Ω–æ–º–µ—Ä —Ö–æ–ª–æ–¥–∏–ª—å–Ω–∏–∫–∞ (–Ω–∞—á–∏–Ω–∞—è —Å 1)

    # –í—ã–≤–æ–¥–∏–º –Ω–æ–º–µ—Ä–∞ –∑–∞—Ä–∞–∂–µ–Ω–Ω—ã—Ö —Ö–æ–ª–æ–¥–∏–ª—å–Ω–∏–∫–æ–≤ —á–µ—Ä–µ–∑ –ø—Ä–æ–±–µ–ª
    print(" ".join(map(str, infected_indices)))


def contains_anton(data):
    target = "anton"
    index = 0  # –ò–Ω–¥–µ–∫—Å –¥–ª—è –æ—Ç—Å–ª–µ–∂–∏–≤–∞–Ω–∏—è –ø–æ–∑–∏—Ü–∏–∏ –≤ —Å—Ç—Ä–æ–∫–µ "anton"

    for char in data:
        if char == target[index]:  # –ï—Å–ª–∏ —Å–∏–º–≤–æ–ª —Å–æ–≤–ø–∞–¥–∞–µ—Ç —Å —Ç–µ–∫—É—â–∏–º —Å–∏–º–≤–æ–ª–æ–º –∏–∑ "anton"
            index += 1  # –ü–µ—Ä–µ—Ö–æ–¥–∏–º –∫ —Å–ª–µ–¥—É—é—â–µ–º—É —Å–∏–º–≤–æ–ª—É "anton"
        if index == len(target):  # –ï—Å–ª–∏ –≤—Å–µ —Å–∏–º–≤–æ–ª—ã "anton" –Ω–∞–π–¥–µ–Ω—ã
            return True

    return False  # –ï—Å–ª–∏ –Ω–µ –≤—Å–µ —Å–∏–º–≤–æ–ª—ã –Ω–∞–π–¥–µ–Ω—ã


