"""Различные элементы
На вход программе подается строка текста, содержащая натуральные числа, расположенные по неубыванию.
Из строки формируется список чисел. Напишите программу для подсчета количества разных элементов в списке.

Формат входных данных
На вход программе подается строка текста, содержащая натуральные числа, разделенные символом пробела,
расположенные по неубыванию.

Формат выходных данных
Программа должна вывести одно число – количество различных элементов списка."""
# Чтение входных данных и преобразование в список чисел
n = list(map(int, input().split()))

# Подсчет количества уникальных элементов
unique_count = 1  # Начальное значение, так как минимум один уникальный элемент точно есть

# Проходим по списку, начиная со второго элемента
for i in range(1, len(n)):
    if n[i] != n[i - 1]:  # Если текущий элемент отличается от предыдущего
        unique_count += 1  # Увеличить счетчик уникальных элементов

# Вывод результата
print(unique_count)

# Второй способ решения задачи - взято из форума
s = input().split()
a = []
for i in s:
    if i not in a:
        a.append(i)
print(len(a))
