"""–•–æ–¥—ã –∫–æ–Ω—è üêé
–ù–∞ —à–∞—Ö–º–∞—Ç–Ω–æ–π –¥–æ—Å–∫–µ 8√ó8 —Å—Ç–æ–∏—Ç –∫–æ–Ω—å. –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –æ—Ç–º–µ—á–∞–µ—Ç –ø–æ–ª–æ–∂–µ–Ω–∏–µ –∫–æ–Ω—è –Ω–∞ –¥–æ—Å–∫–µ –∏ –≤—Å–µ –∫–ª–µ—Ç–∫–∏,
–∫–æ—Ç–æ—Ä—ã–µ –±—å–µ—Ç –∫–æ–Ω—å. –ö–ª–µ—Ç–∫—É, –≥–¥–µ —Å—Ç–æ–∏—Ç –∫–æ–Ω—å, –æ—Ç–º–µ—Ç—å—Ç–µ –∞–Ω–≥–ª–∏–π—Å–∫–æ–π –±—É–∫–≤–æ–π N, –∞ –∫–ª–µ—Ç–∫–∏, –∫–æ—Ç–æ—Ä—ã–µ –±—å–µ—Ç –∫–æ–Ω—å,
–æ—Ç–º–µ—Ç—å—Ç–µ —Å–∏–º–≤–æ–ª–∞–º–∏ *, –æ—Å—Ç–∞–ª—å–Ω—ã–µ –∫–ª–µ—Ç–∫–∏ –∑–∞–ø–æ–ª–Ω–∏—Ç–µ —Ç–æ—á–∫–∞–º–∏.

–§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
–ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞—é—Ç—Å—è –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç—ã –∫–æ–Ω—è –Ω–∞ —à–∞—Ö–º–∞—Ç–Ω–æ–π –¥–æ—Å–∫–µ –≤ —à–∞—Ö–º–∞—Ç–Ω–æ–π –Ω–æ—Ç–∞—Ü–∏–∏ (—Ç–æ –µ—Å—Ç—å –≤ –≤–∏–¥–µ e4,
–≥–¥–µ —Å–Ω–∞—á–∞–ª–∞ –∑–∞–ø–∏—Å—ã–≤–∞–µ—Ç—Å—è –Ω–æ–º–µ—Ä —Å—Ç–æ–ª–±—Ü–∞ (–±—É–∫–≤–∞ –æ—Ç a –¥–æ h, —Å–ª–µ–≤–∞ –Ω–∞–ø—Ä–∞–≤–æ), –∑–∞—Ç–µ–º –Ω–æ–º–µ—Ä—É —Å—Ç—Ä–æ–∫–∏ (—Ü–∏—Ñ—Ä–∞ –æ—Ç 1 –¥–æ 8, —Å–Ω–∏–∑—É –≤–≤–µ—Ä—Ö)).

–§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
–ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ –Ω–∞ —ç–∫—Ä–∞–Ω –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ –¥–æ—Å–∫–∏, —Ä–∞–∑–¥–µ–ª—è—è —ç–ª–µ–º–µ–Ω—Ç—ã –ø—Ä–æ–±–µ–ª–∞–º–∏.

–ü—Ä–∏–º–µ—á–∞–Ω–∏–µ. –®–∞—Ö–º–∞—Ç–Ω–∞—è –¥–æ—Å–∫–∞ –∏–º–µ–µ—Ç –≤–∏–¥:"""

# –í–≤–æ–¥ –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç –∫–æ–Ω—è
knight_position = input("–í–≤–µ–¥–∏—Ç–µ –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç—ã –∫–æ–Ω—è (–Ω–∞–ø—Ä–∏–º–µ—Ä, e4): ")

# –ü—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏–µ —à–∞—Ö–º–∞—Ç–Ω–æ–π –Ω–æ—Ç–∞—Ü–∏–∏ –≤ –∏–Ω–¥–µ–∫—Å—ã
column = ord(knight_position[0]) - ord('a')  # –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º 'a'-'h' –≤ 0-7
row = int(knight_position[1]) - 1  # –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º '1'-'8' –≤ 0-7

# –í–æ–∑–º–æ–∂–Ω—ã–µ —Ö–æ–¥—ã –∫–æ–Ω—è
moves = [
    (2, 1), (2, -1), (-2, 1), (-2, -1),
    (1, 2), (1, -2), (-1, 2), (-1, -2)
]

# –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è —à–∞—Ö–º–∞—Ç–Ω–æ–π –¥–æ—Å–∫–∏
board = [['.' for _ in range(8)] for _ in range(8)]

# –£—Å—Ç–∞–Ω–æ–≤–∫–∞ –ø–æ–∑–∏—Ü–∏–∏ –∫–æ–Ω—è
board[row][column] = 'N'

# –£—Å—Ç–∞–Ω–æ–≤–∫–∞ –∫–ª–µ—Ç–æ–∫, –∫–æ—Ç–æ—Ä—ã–µ –±—å–µ—Ç –∫–æ–Ω—å
for move in moves:
    new_row = row + move[0]
    new_column = column + move[1]
    if 0 <= new_row < 8 and 0 <= new_column < 8:
        board[new_row][new_column] = '*'

# –í—ã–≤–æ–¥ –¥–æ—Å–∫–∏
for row in board:
    print(' '.join(row))
