# Второй способ решения задачи - взято из Гигачат
# Вводим размер матрицы
n = int(input())

# Создаем пустую матрицу размером n x n
matrix = []

# Заполняем матрицу значениями
for _ in range(n):
    row = list(map(int, input().split()))
    matrix.append(row)

# Вычисляем след матрицы
trace = sum(matrix[i][i] for i in range(n))

# Выводим результат
print(trace)

