def pascal(n):
    # –ù–∞—á–∞–ª—å–Ω–∞—è —Å—Ç—Ä–æ–∫–∞ —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫–∞ –ü–∞—Å–∫–∞–ª—è
    row = [1]

    for _ in range(n):
        # –°–æ–∑–¥–∞–µ–º –Ω–æ–≤—É—é —Å—Ç—Ä–æ–∫—É, –Ω–∞—á–∏–Ω–∞—è —Å 1
        new_row = [1]
        # –ó–∞–ø–æ–ª–Ω—è–µ–º –Ω–æ–≤—É—é —Å—Ç—Ä–æ–∫—É
        for j in range(1, len(row)):
            new_row.append(row[j - 1] + row[j])
        # –ó–∞–∫–∞–Ω—á–∏–≤–∞–µ–º —Å—Ç—Ä–æ–∫—É 1
