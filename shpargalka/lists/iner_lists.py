"""–ü–†–ò–ú–ï–† –†–ê–ó–ë–ò–í–ö–ò –¢–ï–ö–°–¢–ê –ù–ê –í–õ–û–ñ–ï–ù–ù–´–ï –°–ü–ò–°–ö–ò - –í–ó–Ø–¢–û –ò–ó –ó–ê–î–ê–ß–ò - 4_3 - 7 –ó–ê–î–ê–ß–ê"""

"""–†–∞–∑–±–∏–µ–Ω–∏–µ –Ω–∞ —á–∞–Ω–∫–∏ üå∂Ô∏è
Sample Input 1:

a b c d e f
2
Sample Output 1:

[['a', 'b'], ['c', 'd'], ['e', 'f']]"""

def chunked(iterable, n):
    """–†–∞–∑–±–∏–≤–∞–µ—Ç —Å–ø–∏—Å–æ–∫ –Ω–∞ —á–∞–Ω–∫–∏ –∑–∞–¥–∞–Ω–Ω–æ–≥–æ —Ä–∞–∑–º–µ—Ä–∞."""
    chunks = []  # –°–ø–∏—Å–æ–∫ –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è —á–∞–Ω–∫–æ–≤
    for i in range(0, len(iterable), n):
        # –î–æ–±–∞–≤–ª—è–µ–º –≤ —Å–ø–∏—Å–æ–∫ —á–∞–Ω–∫ –∏–∑ n —ç–ª–µ–º–µ–Ω—Ç–æ–≤
        chunks.append(iterable[i:i + n])
    return chunks

# –í–≤–æ–¥ –¥–∞–Ω–Ω—ã—Ö
input_string = input()
n = int(input())

# –§–æ—Ä–º–∏—Ä—É–µ–º —Å–ø–∏—Å–æ–∫ –∏–∑ –≤–≤–µ–¥–µ–Ω–Ω–æ–π —Å—Ç—Ä–æ–∫–∏
elements = input_string.split()

# –í—ã–∑—ã–≤–∞–µ–º —Ñ—É–Ω–∫—Ü–∏—é chunked –∏ –≤—ã–≤–æ–¥–∏–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç
result = chunked(elements, n)
print(result)